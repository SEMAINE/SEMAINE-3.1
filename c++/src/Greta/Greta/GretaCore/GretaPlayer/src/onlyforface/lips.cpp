static int isin(int vi1,int vi2,int vi3,GLint fi[6],int *t)
{

	if (fi[0]==vi1&&fi[1]==vi2&&fi[2]==vi3)
	{
		t[0]=fi[3];
		t[1]=fi[4];
		t[2]=fi[5];
		return 1;
	}
	else
	if (fi[0]==vi1&&fi[2]==vi2&&fi[1]==vi3)
	{
		t[0]=fi[3];
		t[1]=fi[5];
		t[2]=fi[4];
		return 1;
	}
	else
	if (fi[1]==vi1&&fi[0]==vi2&&fi[2]==vi3)
	{
		t[0]=fi[4];
		t[1]=fi[3];
		t[2]=fi[5];
		return 1;
	}
	else
	if (fi[1]==vi1&&fi[2]==vi2&&fi[0]==vi3)
	{
		t[0]=fi[4];
		t[1]=fi[5];
		t[2]=fi[3];
		return 1;
	}
	else
	if (fi[2]==vi1&&fi[1]==vi2&&fi[0]==vi3)
	{
		t[0]=fi[5]; // va sempre qui dentro 
		t[1]=fi[4];
		t[2]=fi[3];
		return 1;
	}
	else 
	if (fi[2]==vi1&&fi[0]==vi2&&fi[1]==vi3)
	{
		t[0]=fi[5];
		t[1]=fi[3];
		t[2]=fi[4];
		return 1;
	}

	return 0;
}

#define FLT_EPSILON 0.00010

#define FLOAT_EQ(x,v) (((v-FLT_EPSILON)<x) && (x <(v+FLT_EPSILON)))

void SearchLips(float *v1,float *v2, float *v3,int *t)
{
	int i,last;
	float vx1,vy1,vz1;
	float vx2,vy2,vz2;
	float vx3,vy3,vz3;
	int vi1,vi2,vi3;
	int result;

	vi1=vi2=vi3=-1;

	vx1=v1[0];
	vy1=v1[1];
	vz1=v1[2];

	vx2=v2[0];
	vy2=v2[1];
	vz2=v2[2];

	vx3=v3[0];
	vy3=v3[1];
	vz3=v3[2];

	last=0;

START:
	for(i=last;i<334;i++)
    {
//		if (i==95)
//			printf("!");

		if (FLOAT_EQ(vertices[i][0],vx1))
			if (FLOAT_EQ(vertices[i][1],vy1))
				if (FLOAT_EQ(vertices[i][2],vz1))
			{
					vi1=i;
			}

		if (FLOAT_EQ(vertices[i][0],vx2))
			if (FLOAT_EQ(vertices[i][1],vy2))
				if (FLOAT_EQ(vertices[i][2],vz2))
				{
					vi2=i;
				}

		if (FLOAT_EQ(vertices[i][0],vx3))
			if (FLOAT_EQ(vertices[i][1],vy3))
				if (FLOAT_EQ(vertices[i][2],vz3))
				{
					vi3=i;
				}

			if (vi1!=-1&&
				vi2!=-1&&
				vi3!=-1)
			{
				last=i+1;
				break;
			}
	}

	result=0;

	for (i=0;i<410;i++)
	{
		if (isin(vi1,vi2,vi3,face_indicies[i],t))
		{
			result=1;
		}
	}

	if (!result)
	{
//		printf("not found!");
		goto START;
	}
}

